# 11) Declare uma variável temperatura com um valor numérico e
# imprima se a temperatura está acima de 30 graus Celsius.


graus = int(input('Digite a temperatur atual: '))

if graus > 30:
    print('Temperatura maior que 30 graus Celsius')

elif graus == 30:
    print('Temperatura igual a 30 graus Celsius')

else:
    print('Temperatura abaixo de 30 graus Celsius')

