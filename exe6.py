# 06) Escreva um programa que verifica se dois números inteiros são iguais e imprime "Iguais"
# ou "Diferentes" com base nessa verificação.

num1 = input('Digite o um número: ')
num2 = input('Digite o outro número: ')


if num1 == num2:
    print(num1 ,'é ingual a', num2)

else:
    print(num1, 'é diferente de', num2)

