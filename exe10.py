# Declare duas variáveis, numero1 e numero2, e verifique se são iguais.
# Imprima "Iguais" se forem, caso contrário, imprima "Diferentes".

num1 = int(input('Digite um número: '))
num2 = int(input('Digite outro número: '))

if num1 == num2:
    print(num1, 'é igual a', num2)

else:
    print(num1, 'é diferente de', num2)
