# 09) Declare uma variável idade com um valor numérico inteiro e,
# em seguida, verifique se a idade é maior ou igual a 18.
# Imprima "Maior de idade" ou "Menor de idade" com base na verificação.

idade = int(input('Digite sua idade: '))

if idade >= 18:
    print('Maior de idade')

else:
    print('Menor de idade')

